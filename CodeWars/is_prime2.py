def is_prime(n):
    # Handle special cases
    if n <= 1:
        return False
    elif n <= 3:
        return True
    elif n % 2 == 0 or n % 3 == 0:
        return False

    # Loop through possible divisors (6k +/- 1)
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6

    # If we get here, n is prime
    return True
